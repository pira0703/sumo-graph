success = 0
failed = 0
for w in wrestlers:
    payload = {
        "wiki_url": w["wiki_url"],
        "birth_date": w["birth_date"],
        "episodes": w["episodes"],
    }
    url = SUPABASE_URL + "/rest/v1/rikishi?id=eq." + w["uuid"]
    data = json.dumps(payload).encode("utf-8")
    req = urllib.request.Request(
        url,
        data=data,
        method="PATCH",
        headers={
            "apikey": SUPABASE_KEY,
            "Authorization": f"Bearer {SUPABASE_KEY}",
            "Content-Type": "application/json",
            "Prefer": "return=representation",
        },
    )
    try:
        with urllib.request.urlopen(req) as res:
            result = json.loads(res.read())
            if result:
                r = result[0]
                print(f"✅ {r['shikona']} ({w['uuid'][:8]})")
                success += 1
            else:
                print(f"⚠️  {w['shikona']}: 空レスポンス（UUID不一致の可能性）")
                failed += 1
    except Exception as e:
        print(f"❌ {w['shikona']}: {e}")
        failed += 1

print(f"\n完了: success={success}, failed={failed}")
